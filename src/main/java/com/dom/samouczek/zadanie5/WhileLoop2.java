/**
 * Create by Sylwester Garstecki
 * Program wypisujący liczby od -10 do 40 używając pętli while.
 */
public class WhileLoop2 {
    public static void main(String[] args) {
        int number = -10;
        while(number <= 40){
            System.out.println(number);
            number++;
        }
    }
}
