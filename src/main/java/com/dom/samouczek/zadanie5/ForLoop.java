/**
 * Create by Sylwester Garstecki
 * Program wypisujący liczby od -10 do 40 używając pętli for.
 */
public class ForLoop {
    public static void main(String[] args) {
        for (int i = -10; i <= 40; i++) {
            System.out.println(i);
        }
    }
}
